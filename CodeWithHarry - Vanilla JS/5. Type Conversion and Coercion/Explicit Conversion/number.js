let res;

// Numeric String to number
res = Number('100');
console.log(res);

// boolean to number
res = Number(false);
console.log(res);

// whitespace to number
res = Number('');
console.log(res);

//pure string to number
res = Number('hello');
console.log(res);

// undefined to number
res = Number(undefined);
console.log(res);